    function generateProceduralTile(id, seed) {
        const matrix = createBlankTile();
        const fillValue = (id % 3) + 1;
        for (let r = 0; r < 8; r++) {
            for (let c = 0; c < 8; c++) {
                const symC = c < 4 ? c : 7 - c;
                if ((r + symC * id) % 3 === 0) {
                    matrix[r][c] = fillValue;
                }
            }
        }
        return matrix;
    }

    // Genera código ASM adaptado para registros GBC (RGBDS syntax)
    function generateFormattedGbcASM(title, genre, palettes) {
        let paletteDataText = '';
        
        // Formatear bytes de paletas para el código ASM
        for (let p = 0; p < 8; p++) {
            paletteDataText += `\n; Paleta Fondo ${p}\n`;
            const pal = palettes.bg[p];
            for (let c = 0; c < 4; c++) {
                const col = pal[c] || [31,31,31];
                const word = (col[2] << 10) | (col[1] << 5) | col[0];
                const low = word & 0xFF;
                const high = (word >> 8) & 0xFF;
                paletteDataText += `    db $${low.toString(16).toUpperCase().padStart(2,'0')}, $${high.toString(16).toUpperCase().padStart(2,'0')} ; RGB: ${col[0]},${col[1]},${col[2]}\n`;
            }
        }

        return `; ====================================================
; JUEGO GENERADO CON IA PARA GAME BOY COLOR: ${title}
; Arquitectura: Nintendo Game Boy Color (CGB)
; Compilador recomendado: RGBDS
; ====================================================

SECTION "Header", ROM0[$0100]
Entrypoint:
    nop
    jp Start

; Registro de compatibilidad GBC obligatorio
    org $0143
    db $80              ; $80 = DMG y GBC compatible (%10000000)

SECTION "MainCode", ROM0[$0150]
Start:
    di                  ; Desactivar interrupciones
    ld sp, $FFFE        ; Inicializar pila

WaitVBlank:
    ldh a, [$FF44]
    cp 144
    jr nz, WaitVBlank

    ; Apagar LCD para poder escribir en VRAM de forma segura
    xor a
    ldh [$FF40], a      ; LCDC = 0

    ; ----------------------------------------------------
    ; CARGAR PALETAS DE FONDO GBC ($FF68-$FF69)
    ; ----------------------------------------------------
    ld a, $80           ; Escribir bit de auto-incremento + dirección 0
    ldh [$FF68], a      ; Registrar BCPS
    ld hl, BgPaletteData
    ld c, $69           ; Registrar BCPD
    ld b, 64            ; 8 paletas * 4 colores * 2 bytes = 64 bytes
LoadBgPalLoop:
    ld a, [hl+]
    ldh [c], a
    dec b
    jr nz, LoadBgPalLoop

    ; ----------------------------------------------------
    ; CARGAR PALETAS DE SPRITES GBC ($FF6A-$FF6B)
    ; ----------------------------------------------------
    ld a, $80
    ldh [$FF6A], a      ; Registrar OCPS
    ld hl, SpritePaletteData
    ld c, $6B           ; Registrar OCPD
    ld b, 64
LoadSpPalLoop:
    ld a, [hl+]
    ldh [c], a
    dec b
    jr nz, LoadSpPalLoop

    ; ----------------------------------------------------
    ; COPIAR GRÁFICOS TILES A VRAM ($8000)
    ; ----------------------------------------------------
    ld hl, TileData
    ld de, $8000
    ld bc, 1024
CopyTiles:
    ld a, [hl+]
    ld [de], a
    inc de
    dec bc
    ld a, b
    or c
    jr nz, CopyTiles

    ; ----------------------------------------------------
    ; COPIAR TILEMAP EN BANCO VRAM 0 ($9800)
    ; ----------------------------------------------------
    xor a
    ldh [$FF4F], a      ; VBK = 0 (Activa VRAM Bank 0 para IDs de tiles)
    ld hl, MapData
    ld de, $9800
    ld bc, 1024
CopyMap:
    ld a, [hl+]
    ld [de], a
    inc de
    dec bc
    ld a, b
    or c
    jr nz, CopyMap

    ; ----------------------------------------------------
    ; COPIAR ATRIBUTOS EN BANCO VRAM 1 ($9800)
    ; ----------------------------------------------------
    ld a, 1
    ldh [$FF4F], a      ; VBK = 1 (Activa VRAM Bank 1 para atributos de color)
    ld hl, AttrData
    ld de, $9800
    ld bc, 1024
CopyAttrs:
    ld a, [hl+]
    ld [de], a
    inc de
    dec bc
    ld a, b
    or c
    jr nz, CopyAttrs

    ; Restaurar VRAM Banco 0 antes de encender pantalla
    xor a
    ldh [$FF4F], a

    ; Limpiar OAM
    ld hl, $FE00
    ld bc, 160
ClearOAM:
    xor a
    ld [hl+], a
    dec bc
    ld a, b
    or c
    jr nz, ClearOAM

    ; Configurar Sprite del Héroe (OAM)
    ld hl, $FE00
    ld a, 80            ; Pos Y
    ld [hl+], a
    ld a, 80            ; Pos X
    ld [hl+], a
    ld a, 1             ; Tile ID 1
    ld [hl+], a
    ld a, 0             ; Sprite usa Paleta 0 y atributos DMG apagados
    ld [hl+], a

    ; Activar LCD
    ld a, %10010011     ; LCDC On, BG On, Sprite On
    ldh [$FF40], a

; Bucle de ejecución del juego
GameLoop:
WaitFrame:
    ldh a, [$FF44]
    cp 144
    jr nz, WaitFrame

    ; ... (Lectura Joypad y movimientos de sprite igual a DMG) ...
    jp GameLoop

; ====================================================
; SECCIÓN DE DATOS
; ====================================================
SECTION "Palettes", ROM0[$1F00]
BgPaletteData:
    ${paletteDataText.trim()}

SpritePaletteData:
    ; (Paletas de sprites guardadas en $1F40)

SECTION "TileData", ROM0[$2000]
TileData:
    ; Formato 2bpp
`;
    }

