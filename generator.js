    // ARTE GAME BOY ADVANCE (16 colores / tile, con degradados y sombreado)
    // El GBA puede mostrar muchos más colores simultáneos que el GBC; aquí se
    // construyen paletas de 16 colores (rampas suaves) y tiles que las aprovechan,
    // dando un acabado claramente más rico que el de 4 colores del GBC.
    // ====================================================
    function lerpColor(a, b, t) {
        return [
            Math.round(a[0] + (b[0] - a[0]) * t),
            Math.round(a[1] + (b[1] - a[1]) * t),
            Math.round(a[2] + (b[2] - a[2]) * t)
        ];
    }

    // Genera una paleta de 16 colores interpolando entre "stops" [posición(0-1), [r,g,b]]
    function ramp16(stops) {
        const out = [];
        for (let i = 0; i < 16; i++) {
            const t = i / 15;
            let s0 = stops[0], s1 = stops[stops.length - 1];
            for (let k = 0; k < stops.length - 1; k++) {
                if (t >= stops[k][0] && t <= stops[k + 1][0]) { s0 = stops[k]; s1 = stops[k + 1]; break; }
            }
            const span = (s1[0] - s0[0]) || 1;
            out.push(lerpColor(s0[1], s1[1], (t - s0[0]) / span));
        }
        return out;
    }

