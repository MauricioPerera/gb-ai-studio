    /**
     * Integración con APIs externas para generar datos JSON compatibles con GBC.
     */
    async function generateGame(prompt, apiKey, provider, ollamaUrl) {
        if (!provider || provider === 'local' || (provider !== 'ollama' && !apiKey)) {
            return new Promise((resolve) => {
                setTimeout(() => {
                    resolve(generateGameLocal(prompt));
                }, 1000);
            });
        }

        try {
            if (provider === 'gemini') {
                return await fetchGeminiGbcAPI(prompt, apiKey);
            } else if (provider === 'openai') {
                return await fetchOpenAIGbcAPI(prompt, apiKey);
            } else if (provider === 'ollama') {
                return await fetchOllamaGbcAPI(prompt, ollamaUrl || 'http://localhost:11434');
            }
        } catch (error) {
            console.error("Error GBC API: ", error);
            return generateGameLocal(prompt + " (GBC API Fallback activada)");
        }
    }

